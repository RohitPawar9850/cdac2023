/*
 * Write a program to find minimum and maximum number as well as 
to add two double numbers using methods of Double.

 */
class A30 {
    public static void main(String[] args) {
        double d = 50.5;
        double d1 = 45.50;
        System.out.println("Min : "+Double.min(d, d1));
        System.out.println("max : "+Double.max(d, d1));
        System.out.println("sum : "+Double.sum(d, d1));

    }
}
