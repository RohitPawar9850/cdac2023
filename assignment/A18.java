/*
 * 18. Write a program to find minimum and maximum number as well as 
to add two integer numbers using methods of Integer.
 */
public class A18 {
    public static void main(String[] args) {
        int num1=10,num2=20;
        System.out.println("Max Number : "+Integer.max(num1, num2));
        System.out.println("Min Number : "+Integer.min(num1, num2));
        System.out.println("Sum of Two Numbers : "+Integer.sum(num1, num2));
    }
}
